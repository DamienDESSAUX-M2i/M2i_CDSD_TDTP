# Afficher l'historique des commits de l'application
git log

# Pour limiter le nombre de commit à afficher
git log -n 1 

# Afficher l'historique sur une ligne
git log --oneline

# Historique détaillé
git log -p 

# Historique groupé par auteurs
git shortlog

# Historique des déplacements du curseur HEAD
git reflog 

# Historique complet avec les statistiques
git log --stat 